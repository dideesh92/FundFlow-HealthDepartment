#!/bin/bash

function createCUSTOMER() {
  echo "Enrolling the CA admin"
  mkdir -p organizations/peerOrganizations/CUSTOMER.fundflow.com/

  export FABRIC_CA_CLIENT_HOME=${PWD}/organizations/peerOrganizations/CUSTOMER.fundflow.com/

  set -x
  fabric-ca-client enroll -u https://admin:adminpw@localhost:7054 --caname ca-CUSTOMER --tls.certfiles "${PWD}/organizations/fabric-ca/CUSTOMER/ca-cert.pem"
  { set +x; } 2>/dev/null

  echo 'NodeOUs:
  Enable: true
  ClientOUIdentifier:
    Certificate: cacerts/localhost-7054-ca-CUSTOMER.pem
    OrganizationalUnitIdentifier: client
  PeerOUIdentifier:
    Certificate: cacerts/localhost-7054-ca-CUSTOMER.pem
    OrganizationalUnitIdentifier: peer
  AdminOUIdentifier:
    Certificate: cacerts/localhost-7054-ca-CUSTOMER.pem
    OrganizationalUnitIdentifier: admin
  OrdererOUIdentifier:
    Certificate: cacerts/localhost-7054-ca-CUSTOMER.pem
    OrganizationalUnitIdentifier: orderer' > "${PWD}/organizations/peerOrganizations/CUSTOMER.fundflow.com/msp/config.yaml"

  # Since the CA serves as both the organization CA and TLS CA, copy the org's root cert that was generated by CA startup into the org level ca and tlsca directories

  # Copy CUSTOMER's CA cert to CUSTOMER's /msp/tlscacerts directory (for use in the channel MSP definition)
  mkdir -p "${PWD}/organizations/peerOrganizations/CUSTOMER.fundflow.com/msp/tlscacerts"
  cp "${PWD}/organizations/fabric-ca/CUSTOMER/ca-cert.pem" "${PWD}/organizations/peerOrganizations/CUSTOMER.fundflow.com/msp/tlscacerts/ca.crt"

  # Copy CUSTOMER's CA cert to CUSTOMER's /tlsca directory (for use by clients)
  mkdir -p "${PWD}/organizations/peerOrganizations/CUSTOMER.fundflow.com/tlsca"
  cp "${PWD}/organizations/fabric-ca/CUSTOMER/ca-cert.pem" "${PWD}/organizations/peerOrganizations/CUSTOMER.fundflow.com/tlsca/tlsca.CUSTOMER.fundflow.com-cert.pem"

  # Copy CUSTOMER's CA cert to CUSTOMER's /ca directory (for use by clients)
  mkdir -p "${PWD}/organizations/peerOrganizations/CUSTOMER.fundflow.com/ca"
  cp "${PWD}/organizations/fabric-ca/CUSTOMER/ca-cert.pem" "${PWD}/organizations/peerOrganizations/CUSTOMER.fundflow.com/ca/ca.CUSTOMER.fundflow.com-cert.pem"

  echo "Registering peer0"
  set -x
  fabric-ca-client register --caname ca-CUSTOMER --id.name peer0 --id.secret peer0pw --id.type peer --tls.certfiles "${PWD}/organizations/fabric-ca/CUSTOMER/ca-cert.pem"
  { set +x; } 2>/dev/null

  echo "Registering user"
  set -x
  fabric-ca-client register --caname ca-CUSTOMER --id.name user1 --id.secret user1pw --id.type client --tls.certfiles "${PWD}/organizations/fabric-ca/CUSTOMER/ca-cert.pem"
  { set +x; } 2>/dev/null

  echo "Registering the org admin"
  set -x
  fabric-ca-client register --caname ca-CUSTOMER --id.name CUSTOMERadmin --id.secret CUSTOMERadminpw --id.type admin --tls.certfiles "${PWD}/organizations/fabric-ca/CUSTOMER/ca-cert.pem"
  { set +x; } 2>/dev/null

  echo "Generating the peer0 msp"
  set -x
  fabric-ca-client enroll -u https://peer0:peer0pw@localhost:7054 --caname ca-CUSTOMER -M "${PWD}/organizations/peerOrganizations/CUSTOMER.fundflow.com/peers/peer0.CUSTOMER.fundflow.com/msp" --tls.certfiles "${PWD}/organizations/fabric-ca/CUSTOMER/ca-cert.pem"
  { set +x; } 2>/dev/null

  cp "${PWD}/organizations/peerOrganizations/CUSTOMER.fundflow.com/msp/config.yaml" "${PWD}/organizations/peerOrganizations/CUSTOMER.fundflow.com/peers/peer0.CUSTOMER.fundflow.com/msp/config.yaml"

  echo "Generating the peer0-tls certificates, use --csr.hosts to specify Subject Alternative Names"
  set -x
  fabric-ca-client enroll -u https://peer0:peer0pw@localhost:7054 --caname ca-CUSTOMER -M "${PWD}/organizations/peerOrganizations/CUSTOMER.fundflow.com/peers/peer0.CUSTOMER.fundflow.com/tls" --enrollment.profile tls --csr.hosts peer0.CUSTOMER.fundflow.com --csr.hosts localhost --tls.certfiles "${PWD}/organizations/fabric-ca/CUSTOMER/ca-cert.pem"
  { set +x; } 2>/dev/null

  # Copy the tls CA cert, server cert, server keystore to well known file names in the peer's tls directory that are referenced by peer startup config
  cp "${PWD}/organizations/peerOrganizations/CUSTOMER.fundflow.com/peers/peer0.CUSTOMER.fundflow.com/tls/tlscacerts/"* "${PWD}/organizations/peerOrganizations/CUSTOMER.fundflow.com/peers/peer0.CUSTOMER.fundflow.com/tls/ca.crt"
  cp "${PWD}/organizations/peerOrganizations/CUSTOMER.fundflow.com/peers/peer0.CUSTOMER.fundflow.com/tls/signcerts/"* "${PWD}/organizations/peerOrganizations/CUSTOMER.fundflow.com/peers/peer0.CUSTOMER.fundflow.com/tls/server.crt"
  cp "${PWD}/organizations/peerOrganizations/CUSTOMER.fundflow.com/peers/peer0.CUSTOMER.fundflow.com/tls/keystore/"* "${PWD}/organizations/peerOrganizations/CUSTOMER.fundflow.com/peers/peer0.CUSTOMER.fundflow.com/tls/server.key"

  echo "Generating the user msp"
  set -x
  fabric-ca-client enroll -u https://user1:user1pw@localhost:7054 --caname ca-CUSTOMER -M "${PWD}/organizations/peerOrganizations/CUSTOMER.fundflow.com/users/User1@CUSTOMER.fundflow.com/msp" --tls.certfiles "${PWD}/organizations/fabric-ca/CUSTOMER/ca-cert.pem"
  { set +x; } 2>/dev/null

  cp "${PWD}/organizations/peerOrganizations/CUSTOMER.fundflow.com/msp/config.yaml" "${PWD}/organizations/peerOrganizations/CUSTOMER.fundflow.com/users/User1@CUSTOMER.fundflow.com/msp/config.yaml"

  echo "Generating the org admin msp"
  set -x
  fabric-ca-client enroll -u https://CUSTOMERadmin:CUSTOMERadminpw@localhost:7054 --caname ca-CUSTOMER -M "${PWD}/organizations/peerOrganizations/CUSTOMER.fundflow.com/users/Admin@CUSTOMER.fundflow.com/msp" --tls.certfiles "${PWD}/organizations/fabric-ca/CUSTOMER/ca-cert.pem"
  { set +x; } 2>/dev/null

  cp "${PWD}/organizations/peerOrganizations/CUSTOMER.fundflow.com/msp/config.yaml" "${PWD}/organizations/peerOrganizations/CUSTOMER.fundflow.com/users/Admin@CUSTOMER.fundflow.com/msp/config.yaml"
}

